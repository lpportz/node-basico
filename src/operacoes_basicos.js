const numero1 = 10
const numero2 = 5


// utilizando o sinal de "+" permite somar valores numericos
const soma = numero1 + numero2
console.log(`${numero1} + ${numero2} = ${soma}`)

// utilizando o sinal de "-" permite subtrair valores numericos
const subtracao = numero1 - numero2
console.log(`${numero1} - ${numero2} = ${subtracao}`)

// utilizando o sinal de "*" permite multiplicar valores numericos
const multi = numero1 * numero2
console.log(`${numero1} * ${numero2} = ${multi}`)

// utilizando o sinal de "/" permite dividir valores numericos
const divi = numero1 / numero2
console.log(`${numero1} / ${numero2} = ${divi}`)

